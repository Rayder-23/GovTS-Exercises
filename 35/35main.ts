let animals = ["Cat", "Dog", "Parrot"]

for(let ani of animals) {
    if (ani == "Dog"){
        console.log("\nDogs are a man's best friend.")
    }
    else if (ani == "Cat"){
        console.log("\nCats are really cute animals.")
    }
    else if (ani == "Parrot"){
        console.log("\nParrots are one of few animals that can Mimic Human Speech.")
    }
}
console.log("\nAll of these animals would be great pets.")